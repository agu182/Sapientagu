package listings;

public class Area {
/**Calculates the area.*/	
		  public static void main(String[] args) {
		     
			 
		  double  radius = 10.8; // radius of circle
		   double pi = 3.1416; // pi, approximately
		  double  area = pi * radius * radius; // compute area

		    System.out.println("Area of circle is " + area);
   }

}
